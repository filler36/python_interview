str1 = "Hello, Python!"  # str1 == str2 -> True
str2 = 'Hello, Python!'
str3 = '''Hello,
Python!'''  # str1 == str3 -> False
str4 = """Hello,
Python!"""  # str3 == str4 -> True
str5 = 'Hello,' \
       ' Python!'  # str1 == str5 -> True

str6 = (
    "Hello, "
    "Python!"
)  # str1 == str6 -> True
